package com.spring.project.professor.vo;


public class StudentVO {
	
	private String userNumber; // 학생 학번
	private String empNumber; //교번
	private int grade;//학년
	private String userName; // 학생이름
	private String userImage; // 학생사진
	private String userCellNum; // 학생전번
	private String userEmail; // 학생이메일
	private String faculty; // 단과대
	private String majorName;// 전공명
	private String userEngName; // 영문이름
	
	
	
	
	
	public String getUserEngName() {
		return userEngName;
	}
	public void setUserEngName(String userEngName) {
		this.userEngName = userEngName;
	}
	public String getUserNumber() {
		return userNumber;
	}
	public void setUserNumber(String userNumber) {
		this.userNumber = userNumber;
	}
	public String getEmpNumber() {
		return empNumber;
	}
	public void setEmpNumber(String empNumber) {
		this.empNumber = empNumber;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserImage() {
		return userImage;
	}
	public void setUserImage(String userImage) {
		this.userImage = userImage;
	}
	public String getUserCellNum() {
		return userCellNum;
	}
	public void setUserCellNum(String userCellNum) {
		this.userCellNum = userCellNum;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getFaculty() {
		return faculty;
	}
	public void setFaculty(String faculty) {
		this.faculty = faculty;
	}
	public String getMajorName() {
		return majorName;
	}
	public void setMajorName(String majorName) {
		this.majorName = majorName;
	}
	
	
	
	
	
	
	

}
